	static getIcons() {
		return this.icons;
	}

	static eventMap = {
		CoffeeCup: "Cup",
		Mug: "Cup",
		Dish: "Plate",
	};

	static getEventList() {
		return Object.keys(this.icons);
	}

	static getIcon(key, notFoundIcon = false) {
		if (this.eventMap[key]) {
			key = this.eventMap[key];
		}

		if (this.icons[key]) {
			return this.icons[key];
		} else if (this.others[key]) {
			return this.others[key];
		} else {
			if (notFoundIcon) {
				return this.icons["NotFound"];
			}
			return null;
		}
	}

	static getColor(key) {
		if (this.colors[key]) {
			return this.colors[key];
		} else {
			return "#BAB8BD";
		}
	}
